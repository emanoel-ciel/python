"""
Tipos de Dados em Python

1. Inteiros (int)
   Definição: Números inteiros, positivos ou negativos, sem parte decimal.

   Sintaxe: 
       x = 10
       y = -5
   
   Exemplo de execução:
       x = 10
       y = -5
       print(x + y)  # Saída: 5

2. Ponto Flutuante (float)
   Definição: Números de ponto flutuante, positivos ou negativos, com parte decimal.
   
   Sintaxe: 
       x = 10.5
       y = -5.75
   
   Exemplo de execução:
       x = 10.5
       y = -5.75
       print(x + y)  # Saída: 4.75

3. Strings (str)
   Definição: Sequências de caracteres, usadas para representar texto.
   
   Sintaxe: 
       s1 = "Hello"
       s2 = 'World'
   
   Exemplo de execução:
       s1 = "Hello"
       s2 = 'World'
       print(s1 + " " + s2)  # Saída: Hello World

4. Booleanos (bool)
   Definição: Valores booleanos que podem ser True (Verdadeiro) ou False (Falso).
   
   Sintaxe: 
       x = True
       y = False
   
   Exemplo de execução:
       x = True
       y = False
       print(x and y)  # Saída: False
       print(x or y)   # Saída: True

5. Listas (list)
   Definição: Coleções ordenadas e mutáveis de itens, que podem ser de tipos diferentes.
   
   Sintaxe: 
       lista = [1, 2, 3, "quatro", 5.0]
   
   Exemplo de execução:
       lista = [1, 2, 3, "quatro", 5.0]
       print(lista[3])  # Saída: quatro

6. Tuplas (tuple)
   Definição: Coleções ordenadas e imutáveis de itens.
   
   Sintaxe: 
       tupla = (1, 2, 3, "quatro", 5.0)
   
   Exemplo de execução:
       tupla = (1, 2, 3, "quatro", 5.0)
       print(tupla[3])  # Saída: quatro

7. Conjuntos (set)
   Definição: Coleções desordenadas de itens únicos.
   
   Sintaxe: 
       conjunto = {1, 2, 3, "quatro", 5.0}
   
   Exemplo de execução:
       conjunto = {1, 2, 3, "quatro", 5.0}
       print(conjunto)  # Saída: {1, 2, 3, 'quatro', 5.0}

8. Dicionários (dict)
   Definição: Coleções desordenadas de pares chave-valor.
   
   Sintaxe: 
       dicionario = {"nome": "João", "idade": 30}
   
   Exemplo de execução:
       dicionario = {"nome": "João", "idade": 30}
       print(dicionario["nome"])  # Saída: João

9. NoneType (None)
   Definição: Tipo especial que representa a ausência de valor.
   
   Sintaxe: 
       x = None
   
   Exemplo de execução:
       x = None
       print(x)  # Saída: None

Observações:
- Todos os exemplos acima podem ser executados diretamente em um interpretador Python.
- É importante lembrar que Python é uma linguagem de tipagem dinâmica, portanto, as variáveis podem mudar de tipo durante a execução do programa.
"""
